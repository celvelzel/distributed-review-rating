            f.write(f"| Blend | Mean | Std |\n|-------|------|-----|\n")
            for name, stats in blend_sim.items():
                f.write(f"| {name} | {stats['mean']:.4f} | {stats['std']:.4f} |\n")
            f.write("\n")

