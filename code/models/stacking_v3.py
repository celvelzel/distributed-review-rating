def stacking_ridge(oof_dict, test_dict, y_train, model_names, log, alpha=1.0):
    """Ridge 回归元学习器，5 折 OOF 评估。

    将基模型 OOF 预测作为特征，拟合 Ridge 回归学习最优混合权重。
    使用独立的 KFold（非基模型的 fold）生成元学习器的 OOF，防止对基模型训练预测过拟合。
    测试预测 = 5 折模型预测的平均（类似 bagging）。
    """
    X_meta_train = np.column_stack([oof_dict[n] for n in model_names]).astype(np.float32)
    X_meta_test = np.column_stack([test_dict[n] for n in model_names]).astype(np.float32)

    oof_preds = np.zeros(len(y_train), dtype=np.float32)
    test_preds = np.zeros(X_meta_test.shape[0], dtype=np.float32)
    fold_rmses = []
    fold_coefs = []

    kf = KFold(n_splits=N_FOLDS, shuffle=True, random_state=RANDOM_SEED)
    for fold_idx, (tr_idx, va_idx) in enumerate(kf.split(X_meta_train), 1):
        ridge = Ridge(alpha=alpha, fit_intercept=True)
        ridge.fit(X_meta_train[tr_idx], y_train[tr_idx])

        va_pred = np.clip(ridge.predict(X_meta_train[va_idx]), 1.0, 5.0)
        oof_preds[va_idx] = va_pred
        fold_rmse = float(np.sqrt(np.mean((va_pred - y_train[va_idx]) ** 2)))
        fold_rmses.append(fold_rmse)

        coefs = {n: float(c) for n, c in zip(model_names, ridge.coef_)}
        fold_coefs.append(coefs)
        # Test predictions: each fold's model predicts on the full test set.
        # Averaging across folds reduces variance in the final test predictions,
        # analogous to bagging the meta-learner.
        test_preds += np.clip(ridge.predict(X_meta_test), 1.0, 5.0) / N_FOLDS

    oof_rmse = float(np.sqrt(np.mean((oof_preds - y_train) ** 2)))
    # Average coefficients across folds
    avg_coefs = {n: float(np.mean([fc[n] for fc in fold_coefs])) for n in model_names}
    log.print(f"  Ridge OOF RMSE: {oof_rmse:.5f}  avg_coefs={{{', '.join(f'{n}: {c:.4f}' for n, c in avg_coefs.items())}}}")
    return oof_preds, test_preds, oof_rmse, {"avg_coefs": avg_coefs, "fold_rmses": fold_rmses, "alpha": alpha}


def stacking_lgb(oof_dict, test_dict, y_train, model_names, log, n_estimators=500):
    """LightGBM meta-learner."""
    X_meta_train = np.column_stack([oof_dict[n] for n in model_names]).astype(np.float32)
    X_meta_test = np.column_stack([test_dict[n] for n in model_names]).astype(np.float32)

    oof_preds = np.zeros(len(y_train), dtype=np.float32)
    test_preds = np.zeros(X_meta_test.shape[0], dtype=np.float32)
    fold_rmses = []
    fold_importances = []

    params = {
        "objective": "regression", "metric": "rmse", "learning_rate": 0.05,
        "num_leaves": 31, "n_estimators": n_estimators, "subsample": 0.8,
        "colsample_bytree": 0.8, "min_child_samples": 50, "reg_alpha": 0.1,
        "reg_lambda": 1.0, "verbose": -1, "random_seed": RANDOM_SEED,
    }

    kf = KFold(n_splits=N_FOLDS, shuffle=True, random_state=RANDOM_SEED)
    for fold_idx, (tr_idx, va_idx) in enumerate(kf.split(X_meta_train), 1):
        ds_tr = lgb.Dataset(X_meta_train[tr_idx], y_train[tr_idx])
        ds_va = lgb.Dataset(X_meta_train[va_idx], y_train[va_idx])

        model = lgb.train(
            params, ds_tr, num_boost_round=n_estimators,
            valid_sets=[ds_va],
            callbacks=[lgb.log_evaluation(100), lgb.early_stopping(50)],
        )

        va_pred = np.clip(model.predict(X_meta_train[va_idx]), 1.0, 5.0)
        oof_preds[va_idx] = va_pred
        fold_rmse = float(np.sqrt(np.mean((va_pred - y_train[va_idx]) ** 2)))
        fold_rmses.append(fold_rmse)
        fold_importances.append(model.feature_importance(importance_type="gain").tolist())
        test_preds += np.clip(model.predict(X_meta_test), 1.0, 5.0) / N_FOLDS
        log.print(f"    LGB-Stack fold {fold_idx}: RMSE={fold_rmse:.5f}  best_iter={model.best_iteration}")

    oof_rmse = float(np.sqrt(np.mean((oof_preds - y_train) ** 2)))
    avg_imp = {n: float(np.mean([fi[i] for fi in fold_importances]))
               for i, n in enumerate(model_names)}
    log.print(f"  LGB-Stack OOF RMSE: {oof_rmse:.5f}  importance={{{', '.join(f'{n}: {v:.1f}' for n, v in sorted(avg_imp.items(), key=lambda x: -x[1]))}}}")
    return oof_preds, test_preds, oof_rmse, {"feature_importance": avg_imp, "fold_rmses": fold_rmses}


def stacking_catboost(oof_dict, test_dict, y_train, model_names, log):
    """CatBoost meta-learner."""
    from catboost import CatBoostRegressor, Pool

    X_meta_train = np.column_stack([oof_dict[n] for n in model_names]).astype(np.float32)
    X_meta_test = np.column_stack([test_dict[n] for n in model_names]).astype(np.float32)

    oof_preds = np.zeros(len(y_train), dtype=np.float32)
    test_preds = np.zeros(X_meta_test.shape[0], dtype=np.float32)
    fold_rmses = []
    fold_importances = []

    kf = KFold(n_splits=N_FOLDS, shuffle=True, random_state=RANDOM_SEED)
    for fold_idx, (tr_idx, va_idx) in enumerate(kf.split(X_meta_train), 1):
        model = CatBoostRegressor(
            depth=4, learning_rate=0.05, iterations=300, l2_leaf_reg=3.0,
            loss_function="RMSE", random_seed=RANDOM_SEED, verbose=0,
            early_stopping_rounds=50,
        )
        model.fit(
            X_meta_train[tr_idx], y_train[tr_idx],
            eval_set=Pool(X_meta_train[va_idx], y_train[va_idx]),
            use_best_model=True,
        )

        va_pred = np.clip(model.predict(X_meta_train[va_idx]), 1.0, 5.0)
        oof_preds[va_idx] = va_pred
        fold_rmse = float(np.sqrt(np.mean((va_pred - y_train[va_idx]) ** 2)))
        fold_rmses.append(fold_rmse)
        fold_importances.append(model.get_feature_importance().tolist())
        test_preds += np.clip(model.predict(X_meta_test), 1.0, 5.0) / N_FOLDS
        log.print(f"    CatBoost-Stack fold {fold_idx}: RMSE={fold_rmse:.5f}  best_iter={model.get_best_iteration()}")

    oof_rmse = float(np.sqrt(np.mean((oof_preds - y_train) ** 2)))
    avg_imp = {n: float(np.mean([fi[i] for fi in fold_importances]))
               for i, n in enumerate(model_names)}
    log.print(f"  CatBoost-Stack OOF RMSE: {oof_rmse:.5f}  importance={{{', '.join(f'{n}: {v:.2f}' for n, v in sorted(avg_imp.items(), key=lambda x: -x[1]))}}}")
    return oof_preds, test_preds, oof_rmse, {"feature_importance": avg_imp, "fold_rmses": fold_rmses}


def stacking_elasticnet(oof_dict, test_dict, y_train, model_names, log):
    """ElasticNet meta-learner with automatic alpha/l1_ratio selection."""
    X_meta_train = np.column_stack([oof_dict[n] for n in model_names]).astype(np.float32)
    X_meta_test = np.column_stack([test_dict[n] for n in model_names]).astype(np.float32)

    oof_preds = np.zeros(len(y_train), dtype=np.float32)
    test_preds = np.zeros(X_meta_test.shape[0], dtype=np.float32)
    fold_rmses = []
    fold_coefs = []
    fold_params = []

    kf = KFold(n_splits=N_FOLDS, shuffle=True, random_state=RANDOM_SEED)
    for fold_idx, (tr_idx, va_idx) in enumerate(kf.split(X_meta_train), 1):
        enet = ElasticNetCV(
            l1_ratio=[0.1, 0.3, 0.5, 0.7, 0.9],
            alphas=[0.001, 0.01, 0.1, 1.0, 10.0],
            cv=3, fit_intercept=True, max_iter=5000,
        )
        enet.fit(X_meta_train[tr_idx], y_train[tr_idx])

        va_pred = np.clip(enet.predict(X_meta_train[va_idx]), 1.0, 5.0)
        oof_preds[va_idx] = va_pred
        fold_rmse = float(np.sqrt(np.mean((va_pred - y_train[va_idx]) ** 2)))
        fold_rmses.append(fold_rmse)

        coefs = {n: float(c) for n, c in zip(model_names, enet.coef_)}
        fold_coefs.append(coefs)
        fold_params.append({"alpha": float(enet.alpha_), "l1_ratio": float(enet.l1_ratio_)})
        test_preds += np.clip(enet.predict(X_meta_test), 1.0, 5.0) / N_FOLDS

        coefs_str = {n: f"{c:.4f}" for n, c in coefs.items()}
        log.print(f"    ElasticNet fold {fold_idx}: RMSE={fold_rmse:.5f}  alpha={enet.alpha_:.4f} l1={enet.l1_ratio_:.2f}  coefs={coefs_str}")

    oof_rmse = float(np.sqrt(np.mean((oof_preds - y_train) ** 2)))
    avg_coefs = {n: float(np.mean([fc[n] for fc in fold_coefs])) for n in model_names}
    log.print(f"  ElasticNet OOF RMSE: {oof_rmse:.5f}  avg_coefs={{{', '.join(f'{n}: {c:.4f}' for n, c in avg_coefs.items())}}}")
    return oof_preds, test_preds, oof_rmse, {"avg_coefs": avg_coefs, "fold_rmses": fold_rmses, "params": fold_params}


