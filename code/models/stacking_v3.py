#!/usr/bin/env python
"""
Stacking v3: Expanded base models + diverse meta-learners.

Base models (9 columns):
  - 6 from stacking v2: lgb_tfidf, xgboost, mlp, lgb_safe_dense, xgboost_safe, catboost_safe
  - 1 existing ensemble: ensemble_diverse
  - 2 graph feature models: xgb_graph_safe, lgb_graph_safe

Meta-learners (5 candidates, auto-select best OOF RMSE):
  1. Ridge (alpha=1.0)
  2. LightGBM
  3. CatBoost
  4. ElasticNet (grid search)
  5. Ridge + LGB blend

Outputs:
  - artifacts/models/stacking_v3_oof.npy, stacking_v3_test.npy
  - artifacts/models/stacking_v3_results.json  (structured log for traceability)
  - docs/changelog/stacking-v3-results.md      (human-readable report)
"""

from __future__ import annotations

import json
import sys
import time
from datetime import datetime
from pathlib import Path

import lightgbm as lgb
import numpy as np
import pandas as pd
from sklearn.linear_model import ElasticNetCV, Ridge
from sklearn.model_selection import KFold

ROOT = Path(__file__).resolve().parents[2]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

MODEL_DIR = ROOT / "artifacts" / "models"
FEAT_DIR = ROOT / "artifacts" / "features"
ETL_DIR = ROOT / "artifacts" / "etl"
OUTPUT_DIR = ROOT / "output"
DOCS_DIR = ROOT / "docs" / "changelog"

RANDOM_SEED = 42
N_FOLDS = 5

# ── 基模型注册表 (9 个): 每个模型提供 OOF 和测试预测两列 ──
# 来源: stacking_v2 原有 6 个 + ensemble_diverse 1 个 + 图特征模型 2 个
BASE_MODELS = {
    # Stacking v2 originals
    "lgb_tfidf": {"oof": MODEL_DIR / "lgb_tfidf_oof.npy", "test": MODEL_DIR / "lgb_tfidf_test.npy"},
    "xgboost": {"oof": MODEL_DIR / "xgboost_oof.npy", "test": MODEL_DIR / "xgboost_test.npy"},
    "mlp": {"oof": MODEL_DIR / "mlp_oof.npy", "test": MODEL_DIR / "mlp_test.npy"},
    "lgb_safe_dense": {"oof": MODEL_DIR / "lgb_safe_dense_oof.npy", "test": MODEL_DIR / "lgb_safe_dense_test.npy"},
    "xgboost_safe": {"oof": MODEL_DIR / "xgboost_safe_oof.npy", "test": MODEL_DIR / "xgboost_safe_test.npy"},
    "catboost_safe": {"oof": MODEL_DIR / "catboost_safe_oof.npy", "test": MODEL_DIR / "catboost_safe_test.npy"},
    # New: existing ensembles
    "ensemble_diverse": {"oof": MODEL_DIR / "ensemble_diverse_oof.npy", "test": MODEL_DIR / "ensemble_diverse_test.npy"},
    # Note: lgb_safe_v2 has OOF but NO test prediction file.
    # If regenerated on HPC with test output, re-add here.
    # New: graph feature models (generated by train_graph_models.py)
    "xgb_graph_safe": {"oof": MODEL_DIR / "xgb_graph_safe_oof.npy", "test": MODEL_DIR / "xgb_graph_safe_test.npy"},
    "lgb_graph_safe": {"oof": MODEL_DIR / "lgb_graph_safe_oof.npy", "test": MODEL_DIR / "lgb_graph_safe_test.npy"},
}


class Logger:
    """Dual writer: stdout + file."""
    def __init__(self, filepath):
        self.file = open(filepath, "w", encoding="utf-8")
    def print(self, *args, **kwargs):
        msg = " ".join(str(a) for a in args)
        print(msg, **kwargs)
        self.file.write(msg + "\n")
        self.file.flush()
    def close(self):
        self.file.close()


def load_data():
    y_train = np.load(str(FEAT_DIR / "y_train.npy")).astype(np.float32)
    test_ids = pd.read_parquet(ETL_DIR / "test.parquet", columns=["id"])["id"].values
    return y_train, test_ids


def load_oof_predictions(log):
    """加载所有基模型的 OOF/测试预测，跳过缺失文件或形状不匹配的模型。"""
    # 构建 OOF 矩阵: 每列是一个基模型的 OOF 预测，行数 = 训练集大小
    oof_dict, test_dict = {}, {}
    y_len = np.load(str(FEAT_DIR / "y_train.npy")).shape[0]
    skipped = []

    for name, paths in BASE_MODELS.items():
        oof_path, test_path = paths["oof"], paths["test"]
        if not oof_path.exists():
            log.print(f"  SKIP {name}: OOF file missing ({oof_path.name})")
            skipped.append((name, "oof_missing"))
            continue
        if not test_path.exists():
            log.print(f"  SKIP {name}: test file missing ({test_path.name})")
            skipped.append((name, "test_missing"))
            continue
        oof = np.load(str(oof_path)).astype(np.float32)
        test = np.load(str(test_path)).astype(np.float32)
        if oof.shape[0] != y_len:
            log.print(f"  SKIP {name}: OOF shape {oof.shape} != expected ({y_len},)")
            skipped.append((name, "shape_mismatch"))
            continue
        oof_dict[name] = oof
        test_dict[name] = test
        log.print(f"  Loaded {name}: OOF={oof.shape}, test={test.shape}")

    return oof_dict, test_dict, skipped


