def main():
    t_start = time.perf_counter()
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")

    MODEL_DIR.mkdir(parents=True, exist_ok=True)
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    DOCS_DIR.mkdir(parents=True, exist_ok=True)

    log = Logger(MODEL_DIR / f"stacking_v3_run_{timestamp}.log")
    log.print("=" * 70)
    log.print(f"Stacking v3: Expanded base models + diverse meta-learners")
    log.print(f"Timestamp: {datetime.now().isoformat()}")
    log.print("=" * 70)

    y_train, test_ids = load_data()
    log.print(f"\ny_train: shape={y_train.shape}, mean={y_train.mean():.4f}, std={y_train.std():.4f}")

    log.print("\n--- Loading base model predictions ---")
    oof_dict, test_dict, skipped = load_oof_predictions(log)
    model_names = list(oof_dict.keys())
    n_models = len(model_names)
    log.print(f"\nActive base models: {n_models} / {len(BASE_MODELS)}")
    if skipped:
        log.print(f"Skipped: {skipped}")

    if n_models < 3:
        log.print("ERROR: Need at least 3 base models. Aborting.")
        log.close()
        return

    # Individual model RMSEs + stats
    log.print("\n--- Individual model stats ---")
    individual_stats = {}
    for name in model_names:
        oof = oof_dict[name]
        rmse = float(np.sqrt(np.mean((oof - y_train) ** 2)))
        individual_stats[name] = {
            "oof_rmse": rmse, "mean": float(oof.mean()),
            "std": float(oof.std()), "min": float(oof.min()), "max": float(oof.max()),
        }
        log.print(f"  {name:20s}: RMSE={rmse:.5f}  mean={oof.mean():.4f}  std={oof.std():.4f}")

    # ── Run all meta-learners ──
    results = {}
    meta_details = {}

    log.print("\n--- [1/5] Ridge meta-learner ---")
    ridge_oof, ridge_test, ridge_rmse, ridge_detail = stacking_ridge(oof_dict, test_dict, y_train, model_names, log)
    results["ridge"] = (ridge_oof, ridge_test, ridge_rmse)
    meta_details["ridge"] = ridge_detail

    log.print("\n--- [2/5] LightGBM meta-learner ---")
    lgb_oof, lgb_test, lgb_rmse, lgb_detail = stacking_lgb(oof_dict, test_dict, y_train, model_names, log)
    results["lgb"] = (lgb_oof, lgb_test, lgb_rmse)
    meta_details["lgb"] = lgb_detail

    log.print("\n--- [3/5] CatBoost meta-learner ---")
    try:
        cb_oof, cb_test, cb_rmse, cb_detail = stacking_catboost(oof_dict, test_dict, y_train, model_names, log)
        results["catboost"] = (cb_oof, cb_test, cb_rmse)
        meta_details["catboost"] = cb_detail
    except ImportError:
        log.print("  SKIP: catboost not installed")

    log.print("\n--- [4/5] ElasticNet meta-learner ---")
    enet_oof, enet_test, enet_rmse, enet_detail = stacking_elasticnet(oof_dict, test_dict, y_train, model_names, log)
    results["elasticnet"] = (enet_oof, enet_test, enet_rmse)
    meta_details["elasticnet"] = enet_detail

    log.print("\n--- [5/5] Ridge + LGB blend ---")
    # 网格搜索 Ridge 与 LGB 元学习器的混合权重 (0%~100% Ridge)
    # Ridge 稳定（线性低方差）+ LGB 捕捉非线性交互，混合可能优于任一单独模型
    best_blend_rmse = float("inf")
    best_w = 0.5
    for w100 in range(0, 101):
        w = w100 / 100.0
        blend_oof = np.clip(w * ridge_oof + (1 - w) * lgb_oof, 1.0, 5.0)
        rmse = float(np.sqrt(np.mean((blend_oof - y_train) ** 2)))
        if rmse < best_blend_rmse:
            best_blend_rmse = rmse
            best_w = w
    results["ridge+lgb"] = (
        np.clip(best_w * ridge_oof + (1 - best_w) * lgb_oof, 1.0, 5.0),
        np.clip(best_w * ridge_test + (1 - best_w) * lgb_test, 1.0, 5.0),
        best_blend_rmse,
    )
    meta_details["ridge+lgb"] = {"best_ridge_weight": best_w, "oof_rmse": best_blend_rmse}
    log.print(f"  Ridge+LGB best: w_ridge={best_w:.2f}, RMSE={best_blend_rmse:.5f}")

    # ── 自动选择: 取 OOF RMSE 最低的元学习器作为最终 stacking 输出 ──
    log.print(f"\n{'=' * 70}")
    log.print("--- Results summary ---")
    best_name = None
    best_rmse = float("inf")
    best_test = None
    best_oof = None

    for name, (oof, test, rmse) in results.items():
        log.print(f"  {name:20s}: OOF RMSE = {rmse:.5f}")
        if rmse < best_rmse:
            best_rmse = rmse
            best_name = name
            best_test = test
            best_oof = oof

    log.print(f"\n  ★ Best meta-learner: {best_name} (RMSE={best_rmse:.5f})")

    # ── Compare with stacking v2 ──
    v2_compare = None
    v2_test_path = MODEL_DIR / "stacking_v2_test.npy"
    if v2_test_path.exists():
        v2_test = np.load(str(v2_test_path)).astype(np.float32)
        diff = np.abs(best_test - v2_test)
        v2_compare = {
            "mean_abs_diff": float(diff.mean()),
            "max_abs_diff": float(diff.max()),
            "correlation": float(np.corrcoef(best_test, v2_test)[0, 1]),
        }
        log.print(f"\n  vs stacking_v2 test predictions:")
        log.print(f"    mean|diff| = {v2_compare['mean_abs_diff']:.5f}")
        log.print(f"    max|diff|  = {v2_compare['max_abs_diff']:.5f}")
        log.print(f"    corr       = {v2_compare['correlation']:.6f}")

    # ── 模拟 DeBERTa 混合（提交前预估）──
    # VE: 将 DeBERTa 预测的方差/均值对齐到训练目标分布，改善 RMSE
    # 混合: w% DeBERTa_VE + (100-w)% Stacking_v3，扫描不同权重
    log.print(f"\n--- DeBERTa 1M blend simulation ---")
    deb_path = MODEL_DIR / "deberta_lora_fold1_test.npy"
    blend_sim = {}
    if deb_path.exists():
        deb_test = np.load(str(deb_path)).astype(np.float32)
        target_std, target_mean = y_train.std(), y_train.mean()
        scale = target_std / deb_test.std()
        # Apply VE: centre → scale → shift, then clip to valid rating range
        deb_ve = np.clip((deb_test - deb_test.mean()) * scale + target_mean, 1.0, 5.0)
        log.print(f"  DeBERTa 1M fold1: mean={deb_test.mean():.4f}, std={deb_test.std():.4f}")
        log.print(f"  After VE:         mean={deb_ve.mean():.4f}, std={deb_ve.std():.4f}, scale={scale:.4f}")

        for w_deb in [95, 90, 85, 80]:
            w_stk = 100 - w_deb
            blend = np.clip(w_deb / 100 * deb_ve + w_stk / 100 * best_test, 1.0, 5.0)
            blend_sim[f"deb_ve{w_deb}_sv3_{w_stk}"] = {
                "mean": float(blend.mean()), "std": float(blend.std()),
            }
            log.print(f"  {w_deb}% DeBERTa_VE + {w_stk}% Stacking_v3: mean={blend.mean():.4f}, std={blend.std():.4f}")
    else:
        log.print("  SKIP: deberta_lora_fold1_test.npy not found")

    # ── Save outputs ──
    log.print(f"\n--- Saving outputs ---")
    np.save(str(MODEL_DIR / "stacking_v3_oof.npy"), best_oof)
    np.save(str(MODEL_DIR / "stacking_v3_test.npy"), best_test)

    submission = pd.DataFrame({"id": test_ids, "rating": best_test})
    sub_path = OUTPUT_DIR / "submission-stacking-v3.csv"
    submission.to_csv(sub_path, index=False)
    log.print(f"  Submission → {sub_path}")

    # Save ALL meta-learner outputs
    for name, (oof, test, rmse) in results.items():
        np.save(str(MODEL_DIR / f"stacking_v3_{name}_oof.npy"), oof)
        np.save(str(MODEL_DIR / f"stacking_v3_{name}_test.npy"), test)
    log.print(f"  All meta-learner OOF/test saved")

    # ── Structured JSON results ──
    total_time = time.perf_counter() - t_start
    json_results = {
        "timestamp": datetime.now().isoformat(),
        "total_time_s": round(total_time, 1),
        "n_base_models": n_models,
        "base_models": model_names,
        "skipped_models": [s[0] for s in skipped],
        "individual_stats": individual_stats,
        "meta_learner_results": {
            name: {"oof_rmse": rmse} for name, (_, _, rmse) in results.items()
        },
        "meta_learner_details": meta_details,
        "best_meta_learner": best_name,
        "best_oof_rmse": best_rmse,
        "stacking_v2_comparison": v2_compare,
        "deberta_blend_simulation": blend_sim,
    }
    json_path = MODEL_DIR / "stacking_v3_results.json"
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(json_results, f, indent=2, ensure_ascii=False)
    log.print(f"  Structured results → {json_path}")

    # ── Markdown report ──
    report_path = DOCS_DIR / "stacking-v3-results.md"
    with open(report_path, "w", encoding="utf-8") as f:
        f.write(f"# Stacking v3 Results\n\n")
